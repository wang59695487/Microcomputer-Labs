		ORG	0000H			
MAIN:	MOV		R1,#0AH		;循环次数
		MOV		R0,#0H		;显示初始值
LOOP:	MOV 	A,R0		;取段码
		MOV 	DPTR,#TABLE 
		MOVC 	A,@A+DPTR 
		MOV 	P0,A		;输出到数码管
		LCALL	DELAY1s		;延时1s
		INC		R0			;显示数字加1
		DJNZ	R1,LOOP		;显示完毕否
		SJMP	MAIN		;循环显示
		
TABLE:  DB 0C0H,0F9H,0A4H,0B0H,99H,92H,82H,0F8H,80H,90H
			
DELAY1s:MOV 	R5,#8		;延时1s
DELOOP3:MOV 	R6,#248
DELOOP2:MOV 	R7,#250
DELOOP1:DJNZ 	R7,DELOOP1
		DJNZ 	R6,DELOOP2
		DJNZ 	R5,DELOOP3	
		RET
		END
			
			